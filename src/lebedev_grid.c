u64 lebedev_num_points(u64 level)
{
    if (level > 31)
    {
        level = 31;
    }
    return lebedev_npoints[level];
}

u64 lebedev_num_points_to_level(u64 num_points)
{
    u64 level = 0;
    while (lebedev_npoints[level] < num_points)
    {
        level++;
    }
    return level;
}

void set_lebedev_weight(f64 *weight_buff, f64 weight, u64 count)
{
    for (u64 i = 0; i < count; ++i)
    {
        weight_buff[i] = weight;
    }
}

void gen_lebedev_points_category_3(f64 *grid, f64 a)
{
    u64 n_points = lebedev_grid_sizes[3] * 3;
    f64 b = sqrt(1.0 - 2.0 * a * a);
    for (int i = 0; i < n_points; ++i)
    {
        grid[i] = lebedev_grid_3_a[i] * a + lebedev_grid_3_b[i] * b;
    }
}

void gen_lebedev_points_category_4(f64 *grid, f64 a)
{
    u64 n_points = lebedev_grid_sizes[4] * 3;
    f64 b = sqrt(1.0 - a * a);
    for (int i = 0; i < n_points; ++i)
    {
        grid[i] = lebedev_grid_4_a[i] * a + lebedev_grid_4_b[i] * b;
    }
}

void gen_lebedev_points_category_5(f64 *grid, f64 a, f64 b)
{
    u64 n_points = lebedev_grid_sizes[5] * 3;
    f64 c = sqrt(1.0 - a * a - b * b);
    for (int i = 0; i < n_points; ++i)
    {
        grid[i] = lebedev_grid_5_a[i] * a + lebedev_grid_5_b[i] * b + lebedev_grid_5_c[i] * c;
    }
}

/* level ([0, 31]): refers to the level-th group of lebedev parameters */
u64 lebedev_gen_grid(f64 *grid, f64 *weights, u64 level)
{
    u64 offset_start = lebedev_parameter_offset[level];
    u64 offset_end = lebedev_parameter_offset[level + 1];
    u64 total_np = 0;
    for (u64 k = offset_start; k < offset_end; ++k)
    {
        u64 category = (u64)lebedev_parameters[k][0];
        u64 n_points = lebedev_grid_sizes[category];
        f64 weight = lebedev_parameters[k][3];
        set_lebedev_weight(weights + total_np, weight, n_points);
        switch (category)
        {
        case 0:
            memcpy(grid + total_np * 3, lebedev_grid_0, n_points * 3 * sizeof(f64));
            break;

        case 1:
            memcpy(grid + total_np * 3, lebedev_grid_1, n_points * 3 * sizeof(f64));
            break;

        case 2:
            memcpy(grid + total_np * 3, lebedev_grid_2, n_points * 3 * sizeof(f64));
            break;

        case 3:
            gen_lebedev_points_category_3(grid + total_np * 3, lebedev_parameters[k][1]);
            break;

        case 4:
            gen_lebedev_points_category_4(grid + total_np * 3, lebedev_parameters[k][1]);
            break;

        case 5:
            gen_lebedev_points_category_5(grid + total_np * 3, lebedev_parameters[k][1], lebedev_parameters[k][2]);
            break;

        default:
            break;
        }

        total_np += n_points;
    }
    return total_np;
}

